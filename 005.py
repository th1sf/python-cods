#Escreva um programa que leia o raio de uma esfera, e calcule o seu volume e área.

#leitura de dados

raio = float(input('Digite o volume: '))


#Calculo

V = (4/3) * 3.1415 * raio ** 3
A = 4 * 3.1415 * raio ** 2

#Resultado

print(f'Volume da Esfera: {V:.2f}\nA area é: {A:.2f}')



